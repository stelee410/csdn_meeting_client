-- ============================================
-- V21: 扩充城市字段长度以支持多个城市
-- 创建时间: 2026-03-24
-- 功能: 将 city_code 和 city_name 从 VARCHAR(20/50) 扩充到 VARCHAR(500)
--       支持存储多个城市编码和名称（如 "130200,130300,130100"）
-- ============================================

ALTER TABLE t_meeting
    MODIFY COLUMN city_code VARCHAR(500) COMMENT '城市编码（支持多个，逗号分隔，如 130200,130300,130100）',
    MODIFY COLUMN city_name VARCHAR(500) COMMENT '城市名称（支持多个，逗号分隔，如 唐山市,秦皇岛市,石家庄市）';

